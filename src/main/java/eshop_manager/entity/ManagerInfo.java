package eshop_manager.entity;

import java.io.Serializable;

public class ManagerInfo implements Serializable{
 
	private static final long serialVersionUID = 1719661830655073183L;
	private int manager_id;
	private String manager_name;
	private String manager_pwd;
	private int manager_role;
	public int getManager_id() {
		return manager_id;
	}
	public void setManager_id(int manager_id) {
		this.manager_id = manager_id;
	}
	public String getManager_name() {
		return manager_name;
	}
	public void setManager_name(String manager_name) {
		this.manager_name = manager_name;
	}
	public String getManager_pwd() {
		return manager_pwd;
	}
	public void setManager_pwd(String manager_pwd) {
		this.manager_pwd = manager_pwd;
	}
	public int getManager_role() {
		return manager_role;
	}
	public void setManager_role(int manager_role) {
		this.manager_role = manager_role;
	}
	
}
